"""
agent/memory.py
===============
Dual-layer persistent memory for the Doomscroll Agent.

Why two layers?
---------------
Different access patterns need different storage primitives:

  Layer 1: JSONL log  (data/posts_log.jsonl)
  -------------------------------------------
  - Append-only, one JSON object per line, human-readable
  - Zero dependencies beyond Python's stdlib
  - Use for: full history replay, debugging, CSV export, deduplication by post_id
  - Limitation: O(n) scan for every query — acceptable up to ~500K posts per file

  Layer 2: ChromaDB vector store  (data/chroma/)
  ------------------------------------------------
  - Local, persistent, embedded — no server process required
  - Embeddings generated by sentence-transformers (all-MiniLM-L6-v2, 384-dim)
    running entirely on CPU — no GPU, no cloud API call, no cost per embedding
  - Use for: semantic similarity search, topic clustering, dedup at scale
  - Limitation: not suitable for production scale beyond ~1M embeddings on a
    single machine; at that point, migrate to Qdrant or Pinecone

Both layers are kept consistent: every store() call writes to both.
The vector store degrades gracefully to a no-op if chromadb or
sentence-transformers are not installed (the JSONL log always works).

Data directory layout
---------------------
  data/
    posts_log.jsonl       one JSON object per scraped post
    insights_log.jsonl    one JSON object per pipeline run's corpus insight
    chroma/               ChromaDB persistent storage directory
    agent.log             Python logging output (written by main.py)

Author : Marc Lapira
Project: Doomscroll Agent — Binox 2026 Take-Home (G2)
"""

from __future__ import annotations

import json
import logging
from datetime import datetime, timezone
from pathlib import Path
from typing import Any
import os

logger = logging.getLogger(__name__)

# ---------------------------------------------------------------------------
# Configurable paths
# ---------------------------------------------------------------------------

# DATA_DIR can be overridden via environment variable for Docker deployments
# where /app/data is mounted as a persistent volume.
DATA_DIR      = Path(os.environ.get("DATA_DIR", "data"))
LOG_FILE      = DATA_DIR / "posts_log.jsonl"
CHROMA_DIR    = DATA_DIR / "chroma"
INSIGHTS_LOG  = DATA_DIR / "insights_log.jsonl"


def _ensure_dirs() -> None:
    """Create the data directory tree if it doesn't exist yet."""
    DATA_DIR.mkdir(parents=True, exist_ok=True)
    CHROMA_DIR.mkdir(parents=True, exist_ok=True)


# ---------------------------------------------------------------------------
# Lazy-loaded optional heavy dependencies
# ---------------------------------------------------------------------------

# These module-level globals are None until the first store/search call.
# Using lazy initialisation avoids paying the startup cost (~2-3 s for
# sentence-transformers model load) when the CLI flags like --stats or
# --search are used without a full pipeline run.
_chroma_client = None
_collection    = None
_embedder      = None


def _get_embedder():
    """
    Return a SentenceTransformer model, loading it on first call.

    Model choice: all-MiniLM-L6-v2
    - 384-dimensional embeddings (compact, fast cosine similarity)
    - ~80 MB on disk
    - Runs on CPU in ~50 ms/sentence — fast enough for batches of 25 posts
    - Strong enough for topic-level semantic grouping (not fine-grained NLI)

    Returns None if sentence-transformers is not installed, allowing the
    pipeline to run in JSON-log-only mode.
    """
    global _embedder
    if _embedder is None:
        try:
            from sentence_transformers import SentenceTransformer
            _embedder = SentenceTransformer("all-MiniLM-L6-v2")
            logger.info("Loaded embedding model: all-MiniLM-L6-v2")
        except (ImportError, Exception) as exc:
            logger.warning(
                "sentence-transformers unavailable (%s); "
                "vector store disabled — falling back to JSON-log-only mode.",
                exc,
            )
    return _embedder


def _get_collection():
    """
    Return the ChromaDB collection, creating it on first call.

    Collection schema:
    - id        : Reddit post_id (deduplication key)
    - embedding : 384-dim float vector from all-MiniLM-L6-v2
    - document  : post.full_text() substring (first 2048 chars)
    - metadata  : flat dict of scalar fields (subreddit, sentiment, score, etc.)

    Using cosine distance (hnsw:space="cosine") because we care about semantic
    direction, not vector magnitude — two posts about the same topic should be
    close regardless of text length.

    Returns None if chromadb is not installed.
    """
    global _chroma_client, _collection
    if _collection is not None:
        return _collection
    try:
        import chromadb
        # PersistentClient stores everything on disk — survives process restarts
        _chroma_client = chromadb.PersistentClient(path=str(CHROMA_DIR))
        _collection = _chroma_client.get_or_create_collection(
            name     = "doomscroll_posts",
            metadata = {"hnsw:space": "cosine"},    # cosine similarity metric
        )
        logger.info(
            "ChromaDB collection ready — %d existing embeddings",
            _collection.count(),
        )
    except (ImportError, Exception) as exc:
        logger.warning("chromadb unavailable (%s); vector store disabled.", exc)
    return _collection


